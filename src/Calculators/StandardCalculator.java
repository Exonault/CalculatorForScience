package Calculators;

public class StandardCalculator {
    public static double sum(double a, double b){return a+b;}
    public static double diff(double a, double b){return a-b;}
    public static double dev(double a, double b){return a/b;}
    public static double multi(double a, double b){return a*b;}
    public static double divisional(double a){return 1/a;}//1/x
}
